public class NumOrdenadores
{
    public static void main(String[] args)
    {
        final String CLASE = "Clase7:";
        int contador = 1;

        final int LIMITE = 24;

        while (contador <= LIMITE)
        {
            System.out.print(CLASE + contador + "\t");
            contador = contador + 1;
        }

    }
}
